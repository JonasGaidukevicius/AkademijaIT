package lt.vtvpmc.ems.akademijait.generic;

public class StringBox {
    private final int left;
    private final String right;

    public StringBox(int left, String right) {
        this.left = left;
        this.right = right;
    }

    public int getLeft() {
        return left;
    }

    public String getRight() {
        return right;
    }


}
